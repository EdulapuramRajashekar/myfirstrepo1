package first;

public class Arthematic {
	
	public static void main(String[] args) {
		int a=20;
		int b=15;
		int add,sub,div,mod,mul;
		add=a+b;
		sub=a-b;
		div=a/b;
		mod=a/b;
		mul=a*b;
		System.out.println("The value of a "+a);
		System.out.println("The value of b "+b);
	
		System.out.println("The value of Addition "+add);
		System.out.println("The value of Subtraction "+sub);
		System.out.println("The value of Divison "+div);
		System.out.println("The value of Modulus "+mod);
		System.out.println("The value of Multiplication "+mul);
	}

}
